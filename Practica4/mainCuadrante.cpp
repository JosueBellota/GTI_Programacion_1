int queCuadrante(Punto p){

    if (p.getX()>0 && p.getY()>0)
    {
        return 1;
    }
    if (p.getX()<0 && p.getY()>0)
    {
        return 2;
    }
    if (p.getX()<0 && p.getY()<0)
    {
        return 3;
    }
    if (p.getX()>0 && p.getY()<0)
    {
        return 4;
    }

}


