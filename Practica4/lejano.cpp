#include <iostream>
#include "Punto.h"

// ---------------------------------------------------
// Punto p1, Punto p2
// -->
// lejano()
// -->
// Punto 
// ---------------------------------------------------

Punto lejano(Punto p1, Punto p2){
    
    Punto p0;

    if(p1.distancia(p0) > p2.distancia(p0)){
        return p1;
    }
    else{
        return p2;
    }

}


// ---------------------------------------------------
// ---------------------------------------------------

int main() {

    Punto p1(2,3);
    Punto p2(-2, -5);

    Punto res = lejano(p1, p2);

    if(res.getX() != -2 && res.getY() != -5){
        std::cout << "error";
    }

   

    return 0;

} // ()

